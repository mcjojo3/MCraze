package mc.sayda.mcraze.world;

/**
 * Defines the game mode for the world.
 */
public enum GameMode {
    SURVIVAL, // Standard mode with waves, hunger, health
    CLASSIC, // "Peaceful" style, no waves, purely building/exploration
    CREATIVE, // (Future) Instant break, flying, invulnerability
    HORDE, // (Future) Constant waves, high difficulty
    SKYBLOCK // Void world with starter island
}
